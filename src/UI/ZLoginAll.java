    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        String username = usernameField.getText();
        String password = new String(passwordField.getPassword());

        UserDAO dao = new UserDAO();
        user user = dao.login(username, password);
        
        if (user == null) {
            JOptionPane.showMessageDialog(this, "Login gagal");
            return;
        }

        UserSession.setUser(user);
        System.out.println(user);
        System.out.println("ROLE = " + user.getRole());
        System.out.println("USER_ID = " + user.getUserId());

        if (user != null) {
            JOptionPane.showMessageDialog(this, 
                "Login berhasil sebagai: " + user.getRole());

            // Buka UI sesuai role
            switch (user.getRole()) {
                case "ADMIN":
                    AFrame AFrame = new AFrame();
                    AFrame.setVisible(true);
                    this.dispose();
                    break;
                case "STUDENT":
                    StudentDAO studentDAO = new StudentDAO();
                    student stud = studentDAO.findByStudentId(user.getUserId());
                    
                    if (stud == null) {
                        JOptionPane.showMessageDialog(this, "Data student tidak ditemukan");
                        return;
                    }
                    
                    UserSession.setStudent(stud);
                    
                    MhsFrame MhsFrame = new MhsFrame();
                    MhsFrame.setVisible(true);
                    this.dispose();
                    break;
                case "LECTURER":
                    LectureDAO lectureDAO = new LectureDAO();
                    lecturer lec = lectureDAO.readLecture(user.getUserId());
                    
                    if (lec == null) {
                        JOptionPane.showMessageDialog(this, "Data dosen tidak ditemukan");
                        return;
                    }
                    
                    UserSession.setLecturer(lec);
                    
                    DoFrame DoFrame = new DoFrame();
                    DoFrame.setVisible(true);
                    this.dispose();
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Role tidak dikenali!");
            }

        } else {
            JOptionPane.showMessageDialog(this, 
                "Username atau password salah, atau akun tidak aktif.");
        }
    }//GEN-LAST:event_loginActionPerformed
   
